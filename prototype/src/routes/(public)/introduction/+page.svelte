        <div class="font-bold">Rätsel, Texte und Reihenfolge:</div>
        <div>An jeder Station erwarten dich <span class="font-semibold">Rätsel, Aufgaben oder Quizfragen</span>. Die Rätsel werden <span class="font-semibold">erst freigeschaltet, wenn du am richtigen Ort angekommen bist.</span></div>
        <div>Die <span class="font-semibold">Texte</span> kannst du:</div>
        <ul class="list-disc pl-6">
            <li>entweder vor Ort lesen</li>
            <li>oder ganz entspannt von zu Hause aus, auch ohne die Stationen abzulaufen</li>
        </ul>
        <div>Manchmal geben dir die Texte <span class="font-semibold">Hinweise zu einzelnen Aufgaben</span>. Das kann helfen, ist aber kein Muss. <span class="font-semibold">Alle Rätsel sind auch ohne das Lesen der Texte lösbar.</span></div>
        <div>Die Stationen sind in der App so angeordnet, dass die Texte einer <span class="font-semibold">historischen Chronologie</span> folgen. Auch die Route ergibt - mit Ausnahme der Hafenstation - einen gut begehbaren Rundgang. Du kannst die Reihenfolge aber auch selbst bestimmen.</div>
        <hr />
        <div class="font-bold">Punkte & Spielweise:</div>
        <div>Für jede richtige Antwort erhältst du <span class="font-semibold">3 Punkte</span>. Falsche Antworten ergeben <span class="font-semibold">0 Punkte</span>. Je nach Rätseltyp gibt es <span class="font-semibold">mehrere richtige Antworten.</span></div>
        <div>Insgesamt kannst du bis zu <span class="font-semibold">190 Punkte</span> erreichen. Die <span class="font-semibold">Punkte</span> aller Stationen werden <span class="font-semibold">zusammengerechnet</span>. Du kannst jederzeit sehen, wie viele Punkte du erreicht hast.</div>
        <div>Es gibt <span class="font-semibold">kein Zeitlimit.</span></div>
        <div>Am Ende erwartet dich eine <span class="font-semibold">freiwillige Reflexionsstation</span>, die dazu einlädt, über das eigene Einkaufsverhalten nachzudenken.</div>
