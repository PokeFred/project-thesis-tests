        <div class="font-bold">Willkommen zur digitalen Schnitzeljagd rund ums Einkaufen!</div>
        <div>Wo und wie wurde früher eingekauft? Wie hat sich das Einkaufen - und damit der Alltag der Menschen - über die Jahrhunderte in Dortmund verändert?</div>
        <div>Diese digitale Schnitzeljagd nimmt dich mit auf eine Reise durch die Dortmunder Einkaufsgeschichte: vom Markt über Warenhäuser und Einkaufsstraßen bis hin zur modernen Shopping-Mall.</div>
        <hr />
        <div class="font-bold">Kurz erklärt:</div>
        <ul class="list-disc pl-6">
            <li>Geh zu einer Station in der Innenstadt</li>
            <li>Die Rätsel werden freigeschaltet, sobald du am richtigen Ort bist</li>
            <li>Beantworte Fragen, löse Rätsel und sammle Punkte</li>
            <li>Lies Hintergrundtexte, wenn du möchtes</li>
            <li>Du kannst die Route frei wählen und jederzeit pausieren</li>
        </ul>
        <hr />
        <div class="font-bold">So funktioniert die Schnitzeljagd:</div>
        <div>Die Route umfasst <span class="font-semibold">8 Stationen in der Innenstadt</span> sowie <span class="font-semibold">eine Station am Hafen</span>. Für die Innenstadt-Route solltest du etwa <span class="font-semibold">1,5 Stunden</span> einplanen. Mit der Hafenstation dauert die Route insgesamt <span class="font-semibold">ca. 2-2,5 Stunden</span>.</div>
        <hr />
        <div class="font-bold">Wenig Zeit? Kein Problem.</div>
        <div>Du kannst die Schnitzeljagd auch in Teilen spielen oder dir <span class="font-semibold">nur einzelne Stationen aussuchen</span>. In der Übersicht siehst du, welche Station wie viele Punkte bringt. So kannst du dir deine eigene Route zusammenstellen.</div>
